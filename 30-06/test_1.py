
"""Operadores arimeticos en Python"""
"""Potencia"""

base = 4
potencia = 2

resultado = base**potencia

print("El resutlado de mi potencia es: {}".format(resultado))

"""Usando la palabra reservada "pow" podemos tambien elevar a una cierta potencia un numero"""
"""Nos pedira 2 valores el primero es la base y el segundo es la potencia a ser elevada la"""

potencia1 = pow(30, 2)
potencia2 = pow(8, 4)

print("El valor de mi potencia es: {}".format(potencia1))
print("El valor de mi potencia es: {}".format(potencia2))